package com.akos.uno.game;

/**
 * Represents the color of a card in the game.
 */
public enum CardColor {
    /**
     * Red color.
     */
    RED, 
    /**
     * Yellow color.
     */
    YELLOW, 
    /**
     * Green color.
     */
    GREEN, 
    /**
     * Blue color.
     */
    BLUE, 
    /**
     * Wild color.
     */
    WILD, 
    /**
     * No color.
     */
    NONE
}
