package com.akos.uno.game;

/**
 * Represents the symbol of a card in the game.
 */
public enum CardSymbol {
    /**
     * Zero symbol.
     */
    ZERO, 
    /**
     * One symbol.
     */
    ONE,
    /**
     * Two symbol.
     */
    TWO, 
    /**
     * Three symbol.
     */
    THREE, 
    /**
     * Four symbol.
     */
    FOUR, 
    /**
     * Five symbol.
     */
    FIVE, 
    /**
     * Six symbol.
     */
    SIX,
    /**
     * Seven symbol.
     */
    SEVEN, 
    /**
     * Eight symbol.
     */
    EIGHT, 
    /**
     * Nine symbol.
     */
    NINE, 
    /**
     * Draw Two symbol.
     */
    DRAW_TWO,
    /**
     * Reverse symbol.
     */ 
    REVERSE, 
    /**
     * Skip symbol.
     */
    SKIP,
    /**
     * Wild symbol.
     */ 
    WILD, 
    /**
     * Wild Draw Four symbol.
     */
    WILD_FOUR,
    /**
     * No symbol.
     */ 
    NONE
}
